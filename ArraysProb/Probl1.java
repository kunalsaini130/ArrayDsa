package ArraysProb;


// odd index multi by 2 and even index add by 10
public class Probl1 {
    public static void main(String arg[]){

        int arr[]={10,20,30,40,50,60};
        for(int i=0;i<arr.length;i++){
            if(i%2==0){
                System.out.println(arr[i]+10);
            }
            else{
                System.out.println(arr[i]*2);
            }

        }}     

    }
    
 

    

